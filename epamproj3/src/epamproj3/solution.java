    static boolean hasCycle(SinglyLinkedListNode head) {
        if(head == null || head.next == null) {
            return false;
        }
        SinglyLinkedListNode temporary_node = head;
    
        for(int i = 0; i < 100; i++) {
            if(temporary_node == null) {
                return false;
            } else {
                temporary_node = temporary_node.next;
            }   
        }   
        return true;
}
